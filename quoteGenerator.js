/***********
 * Variables
 ***********/
//Get the modal element
let quoteModal = document.getElementById('quoteModal')
//Get the modam button
let getQuoteBtn = document.getElementById('getQuoteBtn')
//Get the close button
let closeBtn = document.getElementsByClassName('closeBtn')[0]
//Get modal paragraph to put a new quote in it
let quoteText = document.getElementById('quoteText')
//Get Artists Button
let artistsBtn = document.getElementById('artistsBtn')
//Get Scientists Button
let scientistsBtn = document.getElementById('scientistsBtn')
//Get number of quotes
let inputNumber = document.getElementById('inputNumber')
//Keep track of which button has been clicked
var buttonClicked = ''
//Declare a var and set attribute to it to change the bg
var modalClass = ''

/********************
 * Randomize Function
 ********************/
let randomize = (cat) => {
    let quoteArray = quotes.filter((category) => { return category.category === cat })

    if (quoteArray.length !== 0) {
        let quoteObject = quoteArray[Math.floor(Math.random() * quoteArray.length)]
        return quoteObject
    } else {
        return { quote: '', author: '', category: '' }
    }
}

/***********************************************
 * Focus on the form to get the number of Quotes
 ***********************************************/
focusOnNumberOfQuotes = () => {
    inputNumber.focus({ preventScroll: true })
    inputNumber.setAttribute('class', 'offset-xs-3 col-xs-6 offset-sm-3 col-sm-6 focus')
}
blurNumberOfQuotes = () => {
    inputNumber.blur()
    inputNumber.setAttribute('class', 'offset-xs-3 col-xs-6 offset-sm-3 col-sm-6')
}

/*********************************************
 * Get value from HTML... value of the buttons
 *********************************************/
buttonClickHandler = (event) => {
    buttonClicked = event.target.value
    modalClass = 'modal-content modal-' + event.target.value
    focusOnNumberOfQuotes()
}
artistsBtn.addEventListener('click', buttonClickHandler)
scientistsBtn.addEventListener('click', buttonClickHandler)

/*************************************************************
 * Get Quote Function... Open the modal with the quotes inside
 *************************************************************/
openModal = () => {
    //Get value of number of quotes
    let inputValue = document.getElementById('inputNumber').value

    var quote = ''
    if (buttonClicked) {
        for (i = 1; i <= inputValue; i++) {
            let quoteObject = randomize(buttonClicked)
            quote += `<br> "<strong>${quoteObject.quote}</strong>" <br> <footer><cite><small><i>_${quoteObject.author}_</i></small></cite></footer><br>`
        }
        quoteText.innerHTML = quote
        document.getElementById('modal-content').setAttribute('class', modalClass)
        quoteModal.style.display = 'block';
    } else {
        alert('Please select a category from which you want the quote!')
    }

}
getQuoteBtn.addEventListener('click', openModal)


/****************************
 *Function to close the modal
 ****************************/
closeModal = () => {
    quoteModal.style.display = 'none';
    blurNumberOfQuotes()
}
closeBtn.addEventListener('click', closeModal)

/******************************************
 * Function to close modal if click outside
 ******************************************/
clickOutside = (e) => {
    //The target has to be equal whit modal
    //we pass an event parameter
    if (e.target == quoteModal) {
        quoteModal.style.display = 'none'
        blurNumberOfQuotes()
    }
}
window.addEventListener('click', clickOutside)
